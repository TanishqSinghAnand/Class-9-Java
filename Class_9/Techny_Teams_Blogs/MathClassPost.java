package Class_9.Techny_Teams_Blogs;

public class MathClassPost {
    public static void main(String[] args) {
        double a = 30;

        // converting values to radian
        double b = Math.toRadians(a);

        // return the trigonometric sine of a
        System.out.println("Sine of a =  " + Math.sin(a));

        // return the trigonometric cosine value of a
        System.out.println("Cosine of a =  " + Math.cos(a));

        // return the trigonometric tangent value of a
        System.out.println("Tangent of a =  " + Math.tan(a));

        // return the trigonometric arc sine of a
        System.out.println("Sine of a =  " + Math.asin(a));

        // return the trigonometric arc cosine value of a
        System.out.println("Cosine of a =  " + Math.acos(a));

        // return the trigonometric arc tangent value of a
        System.out.println("Tangent of a =  " + Math.atan(a));

        // return the hyperbolic sine of a
        System.out.println("Sine of a =  " + Math.sinh(a));

        // return the hyperbolic cosine value of a
        System.out.println("Cosine of a =  " + Math.cosh(a));

        // return the hyperbolic tangent value of a
        System.out.println("Tangent of a =  " + Math.tanh(a));
    }
}
